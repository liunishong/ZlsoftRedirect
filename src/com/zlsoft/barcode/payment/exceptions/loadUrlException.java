package com.zlsoft.barcode.payment.exceptions;

public class loadUrlException extends Exception {

	private static final long serialVersionUID = 3569502274700979713L;

	public loadUrlException() {
	}

	public loadUrlException(String arg0) {
		super(arg0);
	}

	public loadUrlException(Throwable arg0) {
		super(arg0);
	}

	public loadUrlException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public loadUrlException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
